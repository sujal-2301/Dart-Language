//if , else if and else are conditional statements

/*syntax for these are similar to syntax function where you
  put fname/if (parameter/condition){ body}*/

void main() {
  int x = 7;
  const y = 9;

  if (x == y) {
    print("Both are equal");
  } else if (x > y) {
    print("x is greater than  y");
  } else {
    print("y is greater than x");
  }
}
