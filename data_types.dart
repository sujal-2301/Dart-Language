//dart has many types of data

main() {
  const num = 45;
  //const means the value of variable is constant for the entire program time you cant initate a variable and assign value to it later it must be defined at the time of initializing only

  //final means once a value is assigned to the variable , its value doesnt change for the entire program
  //it can be initiated and assigned value later
  final num2;
  num2 = num * 2;

  print("value of num is $num");
  print("value of num2 is $num2");
}
